def forward_elimination(A, b):
    n = len(A)
    for i in range(n):
        # ???????? ??????? ??????? (???? ?????, ????? ???????? ????????? ?????)
        if A[i][i] == 0:
            for k in range(i + 1, n):
                if A[k][i] != 0:
                    A[i], A[k] = A[k], A[i]
                    b[i], b[k] = b[k], b[i]
                    break
        
        # ?????? ??????? ??????? ????????? ?????? 1 ? ???????? ????
        diag = A[i][i]
        for j in range(i, n):
            A[i][j] /= diag
        b[i] /= diag

        for k in range(i + 1, n):
            factor = A[k][i]
            for j in range(i, n):
                A[k][j] -= factor * A[i][j]
            b[k] -= factor * b[i]

