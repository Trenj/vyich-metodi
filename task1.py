def back_substitution(A, b):
    n = len(A)
    x = [0] * n
    for i in range(n - 1, -1, -1):
        x[i] = b[i] - sum(A[i][j] * x[j] for j in range(i + 1, n))
    return x

def gaussian_elimination(A, b):
    """??????? ???? ??????? ??????"""
    forward_elimination(A, b)
    return back_substitution(A, b)

